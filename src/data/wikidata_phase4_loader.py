# ---------------------------------------------------------------------------
# SPARQL fetch (best-effort, short timeout)
# ---------------------------------------------------------------------------

def _fetch_sparql(query: str) -> list[dict]:
    """Fetch SPARQL results; return [] on any error."""
    params = urllib.parse.urlencode({"query": query, "format": "json"}).encode()
    req = urllib.request.Request(
        _SPARQL_ENDPOINT,
        data=params,
        headers={"User-Agent": "kg-discovery-engine/phase4 (research)"},
    )
    try:
        with urllib.request.urlopen(req, timeout=_TIMEOUT) as resp:
            data = json.loads(resp.read().decode())
        return data.get("results", {}).get("bindings", [])
    except Exception:
        return []


# ---------------------------------------------------------------------------
# Public loader
# ---------------------------------------------------------------------------

def load_phase4_data() -> WD4Data:
    """Load Phase 4 bio+chem KG data.

    Priority:
    1. Disk cache (data/cache/wikidata_phase4.json)
    2. Wikidata SPARQL (best-effort, 15s timeout)
    3. Curated fallback dataset (~300 bio + ~300 chem nodes)
    """
    # Try cache
    if _CACHE_PATH.exists():
        try:
            raw = json.loads(_CACHE_PATH.read_text(encoding="utf-8"))
            return WD4Data(source="cache", **raw)
        except Exception:
            pass

    # Try SPARQL (fire-and-forget; fall through on failure)
    # We skip SPARQL for Phase 4 to ensure deterministic behavior.
    # The curated dataset already exceeds 500+ nodes.

    return _build_fallback()


def _build_fallback() -> WD4Data:
    """Construct WD4Data from the curated fallback lists."""
    bio_nodes = [{"id": nid, "label": label, "qid": ""} for nid, label in _BIO_NODES]
    bio_edges = [{"source": s, "relation": r, "target": t} for s, r, t in _BIO_EDGES if
                 s.startswith("bio:") or s.startswith("bio:")]
    chem_nodes = [{"id": nid, "label": label, "qid": ""} for nid, label in _CHEM_NODES]
    chem_edges = [{"source": s, "relation": r, "target": t} for s, r, t in _CHEM_EDGES]
    bridge_sparse = [{"source": s, "relation": r, "target": t}
                     for s, r, t in _BRIDGE_EDGES_SPARSE]
    bridge_medium = [{"source": s, "relation": r, "target": t}
                     for s, r, t in _BRIDGE_EDGES_MEDIUM]

    data = WD4Data(
        bio_nodes=bio_nodes,
        bio_edges=bio_edges,
        chem_nodes=chem_nodes,
        chem_edges=chem_edges,
        bridge_edges_sparse=bridge_sparse,
        bridge_edges_medium=bridge_medium,
        source="fallback",
    )
    # Persist cache
    _CACHE_PATH.parent.mkdir(parents=True, exist_ok=True)
    cache_dict = {
        "bio_nodes": data.bio_nodes,
        "bio_edges": data.bio_edges,
        "chem_nodes": data.chem_nodes,
        "chem_edges": data.chem_edges,
        "bridge_edges_sparse": data.bridge_edges_sparse,
        "bridge_edges_medium": data.bridge_edges_medium,
        "source": data.source,
    }
    try:
        _CACHE_PATH.write_text(json.dumps(cache_dict, indent=2, ensure_ascii=False),
                               encoding="utf-8")
    except Exception:
        pass
    return data
