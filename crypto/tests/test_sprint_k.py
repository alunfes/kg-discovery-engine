class TestRegimeDominanceGateDecorator:
    """Decorator wraps chain fn: suppresses on R1 match, passes through otherwise."""

    def _make_test_chain(self):
        """Return a chain builder wrapped with R1 for funding_extreme only."""
        call_log = []

        @regime_dominance_gate(
            chain_type="test_decorated_chain",
            regime_signals=["funding_extreme"],
            expected_outcome="beta_reversion",
            contradicting_outcome="positioning_unwind",
        )
        def _chain(kg, merged_kg, collections, a1, a2, corr_nid, break_score, log):
            call_log.append("called")

        return _chain, call_log

    def test_body_not_called_when_r1_fires(self):
        """When R1 suppresses, the chain body must NOT be executed."""
        chain, calls = self._make_test_chain()
        kg = KGraph(family="out")
        merged_kg = _make_kg(funding_extreme_assets=["SOL"])
        collections = _make_collections()
        log: list = []
        chain(kg, merged_kg, collections, "HYPE", "SOL", "corr:HYPE:SOL", 0.5, log)
        assert calls == [], "Chain body must not be called when R1 fires"
        assert log[0]["r1_regime_dominance_gate"] is True

    def test_body_called_when_r1_does_not_fire(self):
        """When regime is absent, the chain body must execute normally."""
        chain, calls = self._make_test_chain()
        kg = KGraph(family="out")
        merged_kg = _make_kg(funding_extreme_assets=[])
        collections = _make_collections()
        log: list = []
        chain(kg, merged_kg, collections, "HYPE", "SOL", "corr:HYPE:SOL", 0.5, log)
        assert calls == ["called"], "Chain body must be called when R1 does not fire"
        assert log == []

    def test_spec_attached_for_introspection(self):
        """Decorated function exposes ._r1_spec for tooling / documentation."""
        chain, _ = self._make_test_chain()
        assert hasattr(chain, "_r1_spec")
        spec = chain._r1_spec
        assert isinstance(spec, R1PolicySpec)
        assert spec.chain_type == "test_decorated_chain"
        assert spec.regime_signals == ["funding_extreme"]

    def test_function_name_preserved(self):
        """@functools.wraps ensures the original function name is preserved."""
        @regime_dominance_gate(chain_type="x", regime_signals=["funding_extreme"])
        def _my_chain(kg, merged_kg, collections, a1, a2, corr_nid, break_score, log):
            pass
        assert _my_chain.__name__ == "_my_chain"


