def _make_kg(
    *,
    funding_extreme_assets: list | None = None,
    aggression_burst_assets: list | None = None,
) -> KGraph:
    """Build minimal KGraph for R1 signal resolution tests."""
    kg = KGraph(family="test_r1")
    for asset in (funding_extreme_assets or []):
        fid = f"funding:{asset}:0"
        kg.add_node(KGNode(fid, "FundingNode", {"asset": asset, "is_extreme": True, "z_score": 3.0}))
    for asset in (aggression_burst_assets or []):
        for i in range(3):
            kg.add_node(KGNode(f"aggr:{asset}:{i}", "AggressionNode", {"is_burst": True}))
    return kg


def _make_collections(
    *,
    oi_accumulation_assets: list | None = None,
    assets: list | None = None,
) -> dict:
    """Build minimal MarketStateCollections for R1 tests."""
    all_assets = assets or ["HYPE", "SOL"]
    accum = set(oi_accumulation_assets or [])
    result = {}
    for a in all_assets:
        oi = [
            OIState(
                asset=a, timestamp_ms=t * 1000,
                oi=1000.0 + t * 10, oi_prev=1000.0,
                oi_change_pct=0.05,
                state_score=0.4 + t * 0.05,
                build_duration=t + 1,
                is_accumulation=(a in accum),
                is_one_sided=(a in accum),
            )
            for t in range(3)
        ]
        result[a] = MarketStateCollection(
            asset=a, run_id="test_r1",
            fundings=[FundingState(asset=a, timestamp_ms=0, funding_rate=0.001,
                                   annualised=0.365, z_score=1.0)],
            oi_states=oi,
        )
    return result


